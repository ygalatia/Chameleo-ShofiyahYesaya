public class StickerDownload {
    public String stickerID;
    public String stickerName;
    public String stickerPath;
    public String stickerFile;

    public String getStickerID() {
        return stickerID;
    }

    public void setStickerID(String stickerID) {
        this.stickerID = stickerID;
    }

    public String getStickerName() {
        return stickerName;
    }

    public void setStickerName(String stickerName) {
        this.stickerName = stickerName;
    }

    public String getStickerPath() {
        return stickerPath;
    }

    public void setStickerPath(String stickerPath) {
        this.stickerPath = stickerPath;
    }

    public String getStickerFile() {
        return stickerFile;
    }

    public void setStickerFile(String stickerFile) {
        this.stickerFile = stickerFile;
    }
}
